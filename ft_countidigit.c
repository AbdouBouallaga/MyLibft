#include "libft.h"

size_t	ft_countidigit(int s)
{
	size_t i;

	if (s < 0)
	{
		i = 1;
		i++;
		s = s * -1;
	}
	else
		i = 0;
	while (s)
	{
		s = s / 10;
		i++;
	}
	return (i);
}
