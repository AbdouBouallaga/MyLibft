#include "libft.h"

size_t	ft_countcdigit(char *str)
{
	size_t	i;

	if (str[0] == '-')
	{
		i = 1;
		i++;
	}
	else
		i = 0;
	while (ft_isdigit(str[i]))
		i++;
	return (i);
}
