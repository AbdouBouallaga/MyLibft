#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*ptr;

	if (!(ptr = (void *)malloc(sizeof(ptr) * size)))
		return (NULL);
	ft_bzero(ptr, size);
	return (ptr);
}
